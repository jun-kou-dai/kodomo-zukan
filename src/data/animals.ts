// ヘルパー関数
export function getAnimalById(id: string): Animal | undefined {
  return animals.find((a) => a.id === id);
}

export function getAnimalsByClass(cls: string): Animal[] {
  return animals.filter((a) => a.classificationClass === cls);
}

export function getAnimalsByHabitat(habitat: string): Animal[] {
  return animals.filter((a) => a.habitat === habitat);
}

export function getAnimalsByTag(tag: string): Animal[] {
  return animals.filter((a) => a.tags.includes(tag as any));
}

export function getFeaturedAnimals(): Animal[] {
  return animals.filter((a) => a.featuredFlag);
}

export function searchAnimals(query: string): Animal[] {
  const q = query.toLowerCase();
  return animals.filter(
    (a) =>
      a.japaneseName.toLowerCase().includes(q) ||
      a.kanaName.includes(q) ||
      a.englishName.toLowerCase().includes(q) ||
      a.scientificName.toLowerCase().includes(q)
  );
}
